	<div class="ellie">
		<p>Our dad doesn’t walk her down the aisle — in fact, he wasn’t invited. But her stepdad does the job, and he’s a very nice man. I’ve never met him before today, but I like him. He runs a boarding house for dogs, and he tells me stories about his favourite guests. He talks to me like I’m not the daughter of a man who once slept with his wife and made her pregnant — a long time ago, mind you, before she was his wife, but still. He tells me about a wolfdog with yellow eyes — a marvelous creature, sleek and fast. She was up every day with the dawn, like a rooster, howling to the sun instead of the moon. “Best wake-up call I’ve ever had,” he says, and his eyes wander over to the dance floor, to the bride with her dress hitched up to her knees. A proud smile splits his broad, rough face. He says, “Christ, she looks pretty!”</p>
		<p class="indent">It’s early summer, and the sun starts to set around nine-thirty. It’s been grey and windy all day, with beams of sunlight escaping every so often from the clouds. By sunset, the clouds have blown to wisps and the bride and groom go down to the river to have their picture taken on the bridge, the coloured sky behind them. The bride has already had a bit too much to drink. Days later, when she looks at that picture, she’ll laugh because her hair is in her face and her groom is holding her upright. Her eyes are closed because she’s smiling so big.</p>
		<p class="indent">At the end of the night I lean into the limousine to say my goodbyes and final congratulations. The bride kisses my cheek and her breath smells pink and sweet, like champagne. The white bumper disappears down the dark road, and slowly, as if roused from a beautiful reverie, the sequined and flowered guests trail out to the parking lot. They slump into their cars, and they go.</p>
		<p class="indent">The following afternoon, when our dad asks me over the phone if she looked happy, I say, “Yes, yes she did.” He says, “What about the weather? Was it okay? Did it rain?” I tell him about the perfect sunset, the turquoise streaks like vapour trails, the quintessential fade from orange to pink. Our dad has always appreciated a good sunset. My intention is not to make him feel like he missed out, but to give him something to hold onto. That last moment of daylight can feel like it goes on forever. We don’t talk about what happens next — the drop into darkness, and quiet.</p>
	
		<p><em>Ellie Sawatzky was raised in the woods of Northwestern Ontario, where she learned the values of bear psychology and the joys of lake swimming. She moved to Vancouver in 2009, and will graduate from UBC this spring with a BFA in creative writing. Her poetry and creative nonfiction has appeared previously in </em>Fugue Magazine<em>, and her first play will be staged in April in the Brave New Play Rites Festival. When she’s not busy writing, you will most likely find her in her kitchen baking bread, in East Van singing with a folk-rock cover choir, or wandering the streets with a borrowed dog.</em></p>
	</div>

	<div class="russell">
		<p>That morning, Father said he was going to Mr. Wong’s to talk business. Father’s grey hair was slicked to one side. It was actually a helmet. Sometimes he took it off and where his brain ought to be, there was a machine like a paper shredder that he would feed newspapers at breakfast.</p>
		<p class="indent">“I will return tonight,” he said. </p>
		<p class="indent">“Will Mother look after me?”</p>
		<p class="indent">“Your mother is sick. Leave her alone. And don’t answer the door.”</p>
		<p class="indent">While Father was at Mr. Wong’s, someone came to the door. They knocked and called, “Hello! Is anyone there?”</p>
		<p class="indent">I was not to answer the door, so I opened the window beside it. Outside stood a young woman.</p>
		<p class="indent">“Hello,” she said. “Will you save the whales?”</p>
		<p class="indent">“The whales?”</p>
		<p class="indent">“They’re dying. Are your parents home?”</p>
		<p class="indent">“Father is at Mr. Wong’s. Mother is sick.”</p>
		<p class="indent">“Well, give them this.” She passed a brochure through the window. “And here —” She gave me a foil, whale-shaped balloon tied to a ribbon. “Remember,” she said. “All life is connected. Hold onto that and don’t let go.”</p>
		<p class="indent">I sat on the floor all day and held the ribbon. The whale bobbed above me and shared its Wisdom From Under The Sea. I asked when Mother would get better. It did not reply.</p>
		<p class="indent">Father returned late. I told him about the dying whales. He scolded me for answering the door. Then he opened the window, snatched away my balloon and shoved it out. “Farewell,” said the whale. “Farewell!”</p>
		<p class="indent">Father lifted his helmet and devoured the brochure. He made a face like he disliked the taste. “Go to bed. Now.”</p>
		<p class="indent">While I brushed my teeth, Father went into my parents’ room. Inside, I heard Mother cry out like she was hurt. I went to the hallway. Water gushed from under their door and over my feet. I was scared and went to bed. Angry yelling woke me and I got up. There was lava pouring from under my parents’ door now. I cried out in fright.</p>
		<p class="indent">Father opened the door. Behind him, in the room, I saw a tropical jungle beach, with waves crashing and bats swooping about.</p>
		<p class="indent">“Your mother is still sick. Do not think on it. Go to bed. Now.”</p>
		<p class="indent">The next morning, the floor was clear of water and lava and nothing came from under my parents’ door. Father ate the economics section for breakfast. </p>
		<p class="indent">“Good,” he said, digesting. “Good. Now, I’m going to Mr. Wong’s again. Don’t answer the door today.”</p>
		<p class="indent">“How is Mother?”</p>
		<p class="indent">Father had left.</p>
		<p class="indent">When I went to my parents’ door, I peered through the keyhole and could see the beach from the night before. But when I opened the door, it was just my parents’ room after all. At first, it looked like someone was lying under the covers, but then the bed was empty and all I saw was a foil whale balloon floating up and up and up.</p>
		<p class="indent">I realized there was no roof. </p>
	
		<p><em>Russell Hirsch is originally from Edmonton, Alberta, and currently in his fourth and final year of the BFA creative writing program at UBC. His stage play “The Last Laugh” will be performed at the Brave New Play Rites student theatre festival in April and his feature film script </em>Resonance<em> is slated for production this fall. To learn more about Russell, check out www.resonancethefilm.com.</em></p>
	</div>

	<div class="kyla">
		<p>I met a woman who told me her secrets. She was sitting on a swing at Kits Beach in the middle of January without a jacket on — bright sun, biting winds and her bare arms. I asked if she wasn’t cold and she said, A little, and asked if she could pet Ranger, my Airedale. Sure, I said, but your hands will —</p>
		<p class="indent">Smell, I know. My family had one when I was a kid, she said, already flailing her arms, dodging Ranger left, right, high, low, then laughing and catching him in a hug when he tried to jump up on her. He likes you, I said. <i>I like you</i>, I thought. Down, boy, I said. </p>
		<p class="indent">I told her I was in the seventh grade the last time I tried to flirt with a girl on a playground. Did you like you better when you were a kid and didn’t need a dog to help you talk to girls? she asked. I realized she was older than I’d thought at first.</p>
		<p class="indent">I told her I liked a lot of things better then. Ranger ran off to relieve himself and I hoped it wasn’t anything I’d have to pick up.</p>
		<p class="indent">Push me, she said.</p>
		<p class="indent">Isn’t it a little cold for swinging? I asked.</p>
		<p class="indent">It’s a little cold for everything, don’t you think?</p>
		<p class="indent">Higher, higher! Her back was warm on my hands and shook when she laughed. The air smelled of cedar — when I asked if she’d noticed this, she laughed harder. </p>
		<p class="indent">What am I missing?</p>
		<p class="indent">You ask a lot of questions, she said. Then she told me that she was full of soil and a forest of cedar saplings grew inside her. A small forest, she said. She kept her arms bare to feed sun from skin to blood to boughs. She didn’t need to breathe like we do because the cedars are always sighing oxygen inside of her; she could inhale all your exhales and breathe oxygen back into you. </p>
		<p class="indent">Could you hold your breath forever? I asked her.</p>
		<p class="indent">Not forever, but a long time. </p>
		<p class="indent">If you meet her you might see only a woman with blue eyes and laugh lines and bitten-down fingernails and not enough layers on for the weather, but hers are hands that can reach down her esophagus and pull up a cedar, root system intact. </p>
		<p class="indent">I asked if she’d ever given one of her cedars away and she said she gave the first one to her father. He planted it on a cliff overlooking the Seymour River. The day a mudslide uprooted and buried it, a police officer knocked on their front door and asked her mother to sit down.</p>
		<p class="indent">After that I stopped asking questions, found a bag dispenser and picked up Ranger’s shit.  All you have to do is ask, and people will tell you. You’ll wonder if maybe their whole lives, they were just waiting for the questions to begin.</p>
	
		<p><em>Kyla Jamieson is a BFA creative writing student who once dreamed of growing cedar saplings in her Brooklyn apartment and gifting them to her dearest friends. This is her third short story since returning from a six-year fiction-writing hiatus. </em></p>
	</div>
	

	<div class="ellie-poetry">
		<p>The morning of your last day — all sweat and hay, a grey ring of sun at the edge of sky, the blind, blue-eyed collie at my heel, earwigs in the bottom of the wheelbarrow.</p>
	
		<p>Dust when your car pulls up. I catch your eye across the yard, the tilt of your shoulders, hitching up work pants on skinny hips. Caught, for one unsteady moment, like a tree pressed forward in a windstorm, then back and on your way, hammer swinging from your hand. </p>
	
		<p>From the barn, the white goat begins to bellow. </p>
	
		<p>Minutes run like mice along the moss-soaked eaves, blood and tissue in webs across the straw, and the white goat with gasping sides tries to stand.</p>
	
		<p>A tangled mess of birth, the first I’ve seen. It leaves me dry, empty, tasting my teeth.</p>
	
		<p>You take my hand, pull me sideways into the abandoned ice cream stand in the woods. We tack a rag to the service window, eat the afternoon in hungry spoonfuls, and when there’s nothing left we lie in silence, twigs cracking, raccoons in the roof. </p>
	
		<p>We’ll go back to wherever we were before summer work, rocky beaches, red sand and mosquitoes, the charcoal smudge of a heron against the sky. We’ll go back there, wash our clothes, cut the tangles from our hair, watch the sun fade from skin over time. </p>
	
		<p>In the almost-dark you pack your car. I watch from the barn and when the trunk slams shut I feel the thud inside my chest. You catch my eye across the yard, and even the goats turn their heads.</p>
	</div>
	

	<div class="michael">
		<p>A walker in the early morning claims to have heard the hum<br>
		 	of a wet finger drawn around the lip of a glass. The fieldnotes<br>
		 	are precarious, they balance on the page like acrobats.</p> 
	
		<p>Two more days of this and you’ll call it quits. Whoever said<br>
			perseverance is beauty made plain never understood our work.<br>
			There is a swallow dancing across the suburb’s low roofs.</p>
	
		<p>There is a starling nesting in your hands when you fall asleep.<br>
			Binoculars were made to mar want with possession; the two<br>
			most obvious things make the most desirable emulsions —</p>
	
		<p>consider an egg dissolving under a whisk. I understand<br>
			why the swallow refused stillness. The walker was puzzled.<br>
			You would not allow yourself to be heard again.</p>
	
		<p><em>Michael Prior is a fourth-year English literature major. He works in a bookstore part-time and writes whenever he gets the chance. His poems have appeared or are forthcoming in </em>Contemporary Verse 2, The Antigonish Review, Qwerty, Branch Magazine<em> and </em>ditch.</p>
	</div>


