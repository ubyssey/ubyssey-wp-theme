		<p>What I liked most about you<br>
			was the alcohol in me;<br>
			foggy, forget-all-else fumes.<br>
			Not stumbling-through-the-snow drunk<br>
			but close.</p>
	
		<p>What I liked most about you<br>
			were the books you promised to lend,<br>
			knowing just what I desired to get me there.<br>
			Sword and sorcery escapism,<br>
			eight months later, still on my shelf.</p>
	
		<p>What I liked most about you<br>
			was that you never seemed to care<br>
			how much we talked or in this case didn’t<br>
			between those monthly midnight visits<br>
			despite being in the same bar every week.</p>
	
		<p>What I liked most about you<br>
			was how your political chatter,<br>
			aggressive and jarring, altogether not like mine,<br>
			fell to the floor with your stupid shirt<br>
			and stayed there till I left.</p>
	
		<p><em>Kirstin Doggart is a creative writing major at UBC, focusing on poetry, fiction, nonfiction and screenwriting. She dwells in Marpole, the little-known neighbourhood of Vancouver, with her better half and her dying plants. She has been published before in </em>Inside Passages, Spill Magazine, Perspectives Magazine<em> and </em>The Ubyssey.</p>
